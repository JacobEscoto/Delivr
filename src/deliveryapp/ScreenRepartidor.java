    private void registerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBtnActionPerformed
        boolean agregado = true;
        String name = nameField.getText();
        String vehiculo = "";
        String seleccion = vehicleBox.getSelectedItem().toString();
        
        if(name.isEmpty() || name.isBlank()) {
            agregado = false;
            JOptionPane.showMessageDialog(this, "Nombre vacio", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
        }
        
        if (seleccion.equals(" ")) {
            agregado = false;
            JOptionPane.showMessageDialog(this, "No elegiste un vehiculo", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
        } else {
            vehiculo = seleccion;
        }
        
        if (!agregado) {
            JOptionPane.showMessageDialog(this, "No se agrego el repartidor dado", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {
            String idRepartidor = mt.generarId(name);
            Repartidor newRepartidor = new Repartidor(name, idRepartidor, vehiculo);
            Dashboard.repartidores.add(newRepartidor);
            JOptionPane.showMessageDialog(this, "Repartidor agregado con exito", "EXITO", JOptionPane.INFORMATION_MESSAGE);
        }
        
        nameField.setText(null);
        vehicleBox.setSelectedItem(" ");
        
        
    }//GEN-LAST:event_registerBtnActionPerformed

