    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        nameLbl.setVisible(false);
        nameField.setVisible(false);
        vehicleLbl.setVisible(false);
        vehicleBox.setVisible(false);
        registerBtn.setVisible(false);
        scrollPane.setVisible(false);
        listaRepartidores.setVisible(false);
        updateLista.setVisible(false);
        updateRepartidor.setVisible(false);
        deleteRepartidor.setVisible(false);
        
        if (Dashboard.repartidores.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No hay repartidores registrados", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
        } else {
            String search = JOptionPane.showInputDialog(this, "Ingrese parametros de busqueda");
            if (search == null || search.isBlank()) {
                return;
            }
            
            String resultado = "";
            
            for(Repartidor repartidor : Dashboard.repartidores) {
                if(repartidor.getNombre().equalsIgnoreCase(search) || repartidor.getIdRepartidor().equals(search) || repartidor.getVehiculo().equalsIgnoreCase(search)) {
                    resultado += repartidor +"\n";
                }
            }
            
            if(resultado.equals("")) {
                JOptionPane.showMessageDialog(this, "No se obtuvieron resultados", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Resultados encontrados!\n" + resultado, "ENCONTRADO", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_searchBtnActionPerformed

